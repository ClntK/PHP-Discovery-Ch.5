
<h1>Creating Dynamic Web Content</h1>

<p><cite>&quot;A Web page that provides custom content for the user based on the results of a search or some other request. Also known as "dynamic HTML" or
"dynamic content," the dynamic term is used when referring to interactive Web pages created for each user in contrast to the billions of static
Web pages that do not change.&quot;</cite> - PC Mag</p>

<p><cite>&quot;Dynamic content is a web-page or email component that changes. Typically, changes are based on user signals that include in-session behavior,
user data, and user-characteristics. In-session behavior- Adapt content based on what pages they visit, which products they add to cart, and how 
long they spend on site.&quot;</cite> - Barilliance.com</p>

<p><cite>&quot;Dynamic Web Pages are written in languages such as CGI, AJAX, ASP, ASP.NET, etc. In dynamic web pages, the Content of pages is different for 
different visitors. It takes more time to load than the static web page. Dynamic web pages are used where the information is changed frequently, 
for example, stock prices, weather information, etc.&quot;</cite> - Geeksforgeeks.com</p><br />

<hr /><br />

<p>As you can see PHP makes it possible to load content that is curated to the users selections or preferences by using the include function to include script
and files that are premade, based on the input the user provides, while also maintiaining the overall layout of the website i.e. the header, footer, navbars, etc.</p>


